-- ─────────────────────────────────────────────────────────────────────────────
-- Table 1: ssr_panel
-- Panel definitions: demographic/psychographic targets with generated personas.
-- ─────────────────────────────────────────────────────────────────────────────

CREATE TABLE ssr_panel (
    id                   UUID        PRIMARY KEY DEFAULT gen_random_uuid(),
    discord_id           TEXT        NOT NULL,
    panel_name           TEXT        NOT NULL,
    product_category     TEXT        NOT NULL,
    demographics         JSONB       NOT NULL DEFAULT '{}'::jsonb,
    psychographics       JSONB,
    panel_size           INTEGER     NOT NULL,
    actual_size          INTEGER,
    custom_instructions  TEXT,
    status               TEXT        NOT NULL DEFAULT 'generating',
    CONSTRAINT ssr_panel_status_valid
        CHECK (status IN ('generating', 'ready', 'partial', 'failed')),
    created_at           TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at           TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

COMMENT ON TABLE ssr_panel IS 'SSR consumer panel definitions: demographic/psychographic targets with generated synthetic personas';
COMMENT ON COLUMN ssr_panel.discord_id IS 'Discord user ID (snowflake) of the panel owner, stored as TEXT';
COMMENT ON COLUMN ssr_panel.demographics IS 'Serialized PersonaDemographics: age_min, age_max, genders, locations, income_brackets, education_levels, languages';
COMMENT ON COLUMN ssr_panel.psychographics IS 'Optional serialized PersonaPsychographics: interests, values, lifestyle_descriptors, media_consumption. NULL if omitted.';
COMMENT ON COLUMN ssr_panel.panel_size IS 'Requested number of personas (5–50 per Pydantic validation)';
COMMENT ON COLUMN ssr_panel.actual_size IS 'Number of successfully generated personas. NULL while generating. May be less than panel_size on partial generation.';
COMMENT ON COLUMN ssr_panel.status IS 'generating: in progress; ready: all personas generated; partial: ≥50% generated; failed: <50% generated (panel not usable, should be deleted)';

CREATE TRIGGER ssr_panel_updated_at
    BEFORE UPDATE ON ssr_panel
    FOR EACH ROW
    EXECUTE FUNCTION update_updated_at_column();

CREATE INDEX IF NOT EXISTS idx_ssr_panel_discord_id ON ssr_panel(discord_id);
CREATE INDEX IF NOT EXISTS idx_ssr_panel_created_at ON ssr_panel(created_at DESC);


-- ─────────────────────────────────────────────────────────────────────────────
-- Table 2: ssr_persona
-- Synthetic consumer personas generated by Claude Haiku for a panel.
-- ─────────────────────────────────────────────────────────────────────────────

CREATE TABLE ssr_persona (
    id              UUID        PRIMARY KEY DEFAULT gen_random_uuid(),
    panel_id        UUID        NOT NULL REFERENCES ssr_panel(id) ON DELETE CASCADE,
    persona_index   INTEGER     NOT NULL,
    name            TEXT        NOT NULL,
    age             INTEGER     NOT NULL,
    CONSTRAINT ssr_persona_age_valid
        CHECK (age >= 18 AND age <= 99),
    location        TEXT        NOT NULL,
    occupation      TEXT        NOT NULL,
    income_bracket  TEXT        NOT NULL,
    CONSTRAINT ssr_persona_income_bracket_valid
        CHECK (income_bracket IN ('low', 'lower_middle', 'middle', 'upper_middle', 'high')),
    education       TEXT        NOT NULL,
    CONSTRAINT ssr_persona_education_valid
        CHECK (education IN ('high_school', 'some_college', 'bachelors', 'graduate', 'postgraduate')),
    summary         TEXT        NOT NULL,
    full_profile    TEXT        NOT NULL,
    created_at      TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    CONSTRAINT ssr_persona_panel_index_unique UNIQUE (panel_id, persona_index)
);

COMMENT ON TABLE ssr_persona IS 'Synthetic consumer personas generated by Claude Haiku for a panel. Reused across multiple runs.';
COMMENT ON COLUMN ssr_persona.persona_index IS '1-based ordinal position within the panel. Unique per panel.';
COMMENT ON COLUMN ssr_persona.income_bracket IS 'Relative income bracket in persona''s country: low, lower_middle, middle, upper_middle, high';
COMMENT ON COLUMN ssr_persona.education IS 'Highest completed education: high_school, some_college, bachelors, graduate, postgraduate';
COMMENT ON COLUMN ssr_persona.summary IS '2–3 sentence human-readable display summary shown in panel_create tool output';
COMMENT ON COLUMN ssr_persona.full_profile IS 'Complete labeled-section Claude Haiku output. Used as system prompt content when inhabiting persona during ssr_panel_run.';

CREATE INDEX IF NOT EXISTS idx_ssr_persona_panel_id ON ssr_persona(panel_id);


-- ─────────────────────────────────────────────────────────────────────────────
-- Table 3: ssr_run
-- A single SSR pipeline execution: one stimulus tested against all panel personas.
-- Contains the stimulus (denormalized for query efficiency — no separate ssr_stimulus table).
-- ─────────────────────────────────────────────────────────────────────────────

CREATE TABLE ssr_run (
    id                    UUID        PRIMARY KEY DEFAULT gen_random_uuid(),
    panel_id              UUID        NOT NULL REFERENCES ssr_panel(id) ON DELETE CASCADE,
    discord_id            TEXT        NOT NULL,
    run_label             TEXT        NOT NULL,
    stimulus              TEXT        NOT NULL,
    stimulus_type         TEXT        NOT NULL,
    CONSTRAINT ssr_run_stimulus_type_valid
        CHECK (stimulus_type IN (
            'ad_copy',
            'headline',
            'tagline',
            'product_concept',
            'brand_message',
            'campaign_theme',
            'influencer_pitch',
            'pricing_message',
            'packaging_description',
            'social_caption'
        )),
    evaluation_dimensions TEXT[]      NOT NULL DEFAULT '{}',
    status                TEXT        NOT NULL DEFAULT 'pending',
    CONSTRAINT ssr_run_status_valid
        CHECK (status IN ('pending', 'running', 'completed', 'failed')),
    personas_scored       INTEGER,
    dimension_means       JSONB,
    started_at            TIMESTAMPTZ,
    completed_at          TIMESTAMPTZ,
    created_at            TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at            TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

COMMENT ON TABLE ssr_run IS 'A single SSR pipeline execution: one stimulus tested against all personas in a panel';
COMMENT ON COLUMN ssr_run.discord_id IS 'Denormalized panel owner for fast ownership checks without joining ssr_panel';
COMMENT ON COLUMN ssr_run.stimulus IS 'Full text of the marketing asset being tested (up to 4000 chars, enforced by Pydantic)';
COMMENT ON COLUMN ssr_run.stimulus_type IS 'Category: ad_copy, headline, tagline, product_concept, brand_message, campaign_theme, influencer_pitch, pricing_message, packaging_description, social_caption';
COMMENT ON COLUMN ssr_run.evaluation_dimensions IS 'Array of dimension names evaluated in this run, e.g. {purchase_intent,message_clarity,overall_appeal}';
COMMENT ON COLUMN ssr_run.status IS 'pending: created; running: pipeline executing; completed: results stored; failed: pipeline errored';
COMMENT ON COLUMN ssr_run.personas_scored IS 'Count of personas that produced scoreable responses. NULL until run completes.';
COMMENT ON COLUMN ssr_run.dimension_means IS 'Quick-access dict of dimension_name → mean_score, e.g. {"purchase_intent": 3.85}. NULL until run completes.';
COMMENT ON COLUMN ssr_run.started_at IS 'When the SSR pipeline began executing. NULL while status=pending.';
COMMENT ON COLUMN ssr_run.completed_at IS 'When the pipeline finished (success or failure). NULL until status=completed or failed.';

CREATE TRIGGER ssr_run_updated_at
    BEFORE UPDATE ON ssr_run
    FOR EACH ROW
    EXECUTE FUNCTION update_updated_at_column();

CREATE INDEX IF NOT EXISTS idx_ssr_run_panel_id ON ssr_run(panel_id, created_at DESC);
CREATE INDEX IF NOT EXISTS idx_ssr_run_discord_id ON ssr_run(discord_id, created_at DESC);
CREATE INDEX IF NOT EXISTS idx_ssr_run_status ON ssr_run(status) WHERE status IN ('pending', 'running');


-- ─────────────────────────────────────────────────────────────────────────────
