    run_id         UUID        NOT NULL REFERENCES ssr_run(id) ON DELETE CASCADE,
    persona_id     UUID        NOT NULL REFERENCES ssr_persona(id) ON DELETE CASCADE,
    response_text  TEXT        NOT NULL,
    created_at     TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    CONSTRAINT ssr_response_run_persona_unique UNIQUE (run_id, persona_id)
);

COMMENT ON TABLE ssr_response IS 'Free-text consumer reactions elicited from each persona for a panel run. One row per (run, persona).';
COMMENT ON COLUMN ssr_response.response_text IS 'Raw Claude Haiku output when inhabiting the persona and reacting to the stimulus. Not parsed or truncated. Typically 300–1800 characters.';

CREATE INDEX IF NOT EXISTS idx_ssr_response_run_id ON ssr_response(run_id);
CREATE INDEX IF NOT EXISTS idx_ssr_response_persona_run ON ssr_response(persona_id, run_id);


-- ─────────────────────────────────────────────────────────────────────────────
-- Table 5: ssr_score
-- Per-persona per-dimension Likert scores. One row per (response, dimension).
-- run_id and persona_id are denormalized from ssr_response for fast run-level queries.
-- ─────────────────────────────────────────────────────────────────────────────

CREATE TABLE ssr_score (
    id                  UUID        PRIMARY KEY DEFAULT gen_random_uuid(),
    response_id         UUID        NOT NULL REFERENCES ssr_response(id) ON DELETE CASCADE,
    run_id              UUID        NOT NULL REFERENCES ssr_run(id) ON DELETE CASCADE,
    persona_id          UUID        NOT NULL REFERENCES ssr_persona(id) ON DELETE CASCADE,
    dimension           TEXT        NOT NULL,
    hard_score          INTEGER     NOT NULL,
    CONSTRAINT ssr_score_hard_score_valid
        CHECK (hard_score >= 1 AND hard_score <= 5),
    weighted_score      FLOAT8      NOT NULL,
    similarities        JSONB       NOT NULL DEFAULT '{}'::jsonb,
    response_embedding  FLOAT8[],
    created_at          TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    CONSTRAINT ssr_score_response_dimension_unique UNIQUE (response_id, dimension)
);

COMMENT ON TABLE ssr_score IS 'Per-persona per-dimension Likert scores. One row per (response, dimension). run_id and persona_id are denormalized for efficient run-level queries.';
COMMENT ON COLUMN ssr_score.run_id IS 'Denormalized run_id from ssr_response.run_id for fast SELECT * WHERE run_id = $1 without join';
COMMENT ON COLUMN ssr_score.persona_id IS 'Denormalized persona_id from ssr_response.persona_id for fast persona-name joins';
COMMENT ON COLUMN ssr_score.dimension IS 'Evaluation dimension name, e.g. purchase_intent, message_clarity. One of the 10 EvaluationDimension values.';
COMMENT ON COLUMN ssr_score.hard_score IS 'Integer Likert score 1–5: argmax of cosine similarities across all 5 anchor embeddings';
COMMENT ON COLUMN ssr_score.weighted_score IS 'Continuous score 1.0–5.0: softmax-weighted mean of anchor scale points using cosine similarities as weights';
COMMENT ON COLUMN ssr_score.similarities IS 'Dict of scale_point → cosine_similarity, e.g. {"1": 0.182, "2": 0.241, "3": 0.389, "4": 0.712, "5": 0.834}. Keys are strings (JSONB requirement).';
COMMENT ON COLUMN ssr_score.response_embedding IS 'Optional 1536-dim float32 embedding of response_text (text-embedding-3-small). NULL by default. Stored for future re-scoring without API calls. 12 KB per row.';

CREATE INDEX IF NOT EXISTS idx_ssr_score_run_id ON ssr_score(run_id);
CREATE INDEX IF NOT EXISTS idx_ssr_score_response_id ON ssr_score(response_id);


